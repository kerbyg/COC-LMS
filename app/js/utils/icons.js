/**
 * @param {string} name icon key or legacy emoji
 * @param {{ size?: number, className?: string, title?: string }} [opts]
 */
export function icon(name, opts = {}) {
    injectStyles();
    const resolved = EMOJI_MAP[name] || name;
    const inner = PATHS[resolved];
    if (!inner) return '';
    const size = opts.size ?? 18;
    const cls = ['ui-icon', opts.className].filter(Boolean).join(' ');
    const title = opts.title ? ` title="${String(opts.title).replace(/"/g, '&quot;')}"` : '';
    return `<span class="${cls}"${title} aria-hidden="true"><svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${inner}</svg></span>`;
}

